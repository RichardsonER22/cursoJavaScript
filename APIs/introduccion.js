/*
APIs del navegador web 
Los navegadores nos dan acceso a diferentes API con las que podemos:
- Local Storage API- trabajar con almacenamiento local
- Fetch API- Hacer peticiones a servidores
- History API- para poder acceder al historial de la pagina
- Validation API- Para hacer uso de las funciones del navegador para validar formularios
- Mas 
*/

/*
APIs externas
Tambien puede haber APIs externas que nos permiten acceder a recursos de otra pagina web.
Ejemplo con la API de youtube:
- Nos permite obtener informacion de videos y canales
- Nos permite subir videos

Estas son cosas que no pdriamos hacer desde nuestra pagina si no existiera una API.
Gracias a que existe una API, podemos hablarle y pedirle obtener datos u hacer operaciones.
*/

